public class VariablesDtype {
    public static void main(String args[]){

        byte byte_number = 10;
        short short_number = 1900;
        int int_number = 1983495000;
        long long_number = 945983408l;

        float float_number = 4234.53345983753f;
        double double_number = 23421523452345.345234523523452345;

        boolean boolean_1 = true;
        boolean boolean_2 = false;

        char character = 'c';

        String str = "Darshan";

        System.out.printf("Byte Number: %d\n", byte_number);
        System.out.printf("Short Number: %d\n", short_number);
        System.out.printf("Integer Number: %d\n", int_number);
        System.out.printf("Long Number: %d\n", long_number);
        System.out.printf("Float Number: %f\n", float_number);
        System.out.printf("Double Number: %f\n", double_number);
        System.out.printf("Boolean 1: %b\n", boolean_1);
        System.out.printf("Boolean 2: %b\n", boolean_1);
        System.out.printf("Character : %c\n", character);
        System.out.printf("String is: %s\n", str);

    }
}
